#!/usr/bin/env node
/* global process, console */
/**
 * gen-api-paths.mjs — Generate apiPaths.generated.ts from merged OpenAPI spec.
 *
 * Reads packages/core/api-spec/openapi.json and produces
 * packages/core/config/apiPaths.generated.ts with typed path constants
 * grouped by service (NODE_API, HOSTING_API, SEARCH_API).
 */
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Params to SKIP encodeURIComponent (known URL-safe by construction).
// All other non-catch-all params are encoded by default for safety.
const SKIP_ENCODE_PARAMS = new Set([
  // Catch-all params are handled separately via {param+} syntax.
  // Add params here ONLY if they are guaranteed URL-safe AND encoding
  // would break the backend (e.g., already percent-decoded by the router).
]);

const GROUPS = [
  {
    id: 'HOSTING_API',
    exportName: 'HOSTING_API_PATHS',
    matchPrefix: '/platform/v1/',
    stripForPath: '',
    stripForName: '/platform/v1/',
  },
  {
    id: 'SEARCH_API',
    exportName: 'SEARCH_API_PATHS',
    matchPrefix: '/search/v1/',
    stripForPath: '',
    stripForName: '/search/v1/',
  },
  {
    id: 'NODE_API',
    exportName: 'NODE_API_PATHS',
    matchPrefix: '/v1/',
    stripForPath: '/v1',
    stripForName: '/v1/',
  },
];

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function extractParams(path) {
  const params = [];
  for (const seg of path.split('/')) {
    const m = seg.match(/^\{(.+?)(\+)?\}$/);
    if (m) params.push({ name: m[1], catchAll: !!m[2] });
  }
  return params;
}

function pathToBaseName(path, stripPrefix) {
  const stripped = path.slice(stripPrefix.length);
  const parts = [];
  for (const seg of stripped.split('/').filter(Boolean)) {
    if (seg.startsWith('{')) continue;
    parts.push(seg.toUpperCase().replace(/-/g, '_'));
  }
  return parts.join('_') || 'ROOT';
}

function paramSuffix(params) {
  return params
    .map((p) => p.name.replace(/([a-z])([A-Z])/g, '$1_$2').toUpperCase())
    .join('_');
}

function buildValue(path, stripForPath, params) {
  const rel = stripForPath ? path.slice(stripForPath.length) : path;
  if (params.length === 0) return JSON.stringify(rel);

  const sig = params.map((p) => `${p.name}: string`).join(', ');
  let tpl = rel;
  for (const p of params) {
    const ph = p.catchAll ? `{${p.name}+}` : `{${p.name}}`;
    const repl = p.catchAll || SKIP_ENCODE_PARAMS.has(p.name)
      ? '${' + p.name + '}'
      : '${encodeURIComponent(' + p.name + ')}';
    tpl = tpl.replace(ph, repl);
  }
  return `(${sig}) => \`${tpl}\``;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const [specArg, outputArg] = process.argv.slice(2);
const specPath = specArg
  ? resolve(process.cwd(), specArg)
  : join(__dirname, '..', 'packages', 'core', 'api-spec', 'openapi.json');
const spec = JSON.parse(readFileSync(specPath, 'utf8'));
const allPaths = Object.keys(spec.paths || {}).sort();

const grouped = {};
for (const g of GROUPS) grouped[g.id] = {};

for (const path of allPaths) {
  const group = GROUPS.find((g) => path.startsWith(g.matchPrefix));
  if (!group) continue;
  const base = pathToBaseName(path, group.stripForName);
  (grouped[group.id][base] ??= []).push(path);
}

// Resolve collisions: static path keeps base name; parameterized gets _BY_ suffix
function resolveCollisions(nameMap) {
  const resolved = new Map();
  for (const [base, paths] of Object.entries(nameMap)) {
    if (paths.length === 1) {
      resolved.set(base, paths[0]);
      continue;
    }
    for (const p of paths) {
      const params = extractParams(p);
      if (params.length === 0) {
        resolved.set(base, p);
      } else {
        let name = `${base}_BY_${paramSuffix(params)}`;
        // Rare: further collision after suffix — append segment count
        if (resolved.has(name)) name += `_${params.length}`;
        resolved.set(name, p);
      }
    }
  }
  return resolved;
}

const lines = [
  '/**',
  ' * AUTO-GENERATED by scripts/gen-api-paths.mjs — DO NOT EDIT',
  ' *',
  ' * Source: packages/core/api-spec/openapi.json (merged OpenAPI spec)',
  ' * Re-run: pnpm --filter @mobazha/core generate:api',
  ' *',
  ' * Path functions accept RAW (unencoded) param values.',
  ' * encodeURIComponent is applied internally — do NOT pre-encode.',
  ' */',
  '',
];

let totalPaths = 0;

for (const group of GROUPS) {
  const resolved = resolveCollisions(grouped[group.id]);
  totalPaths += resolved.size;

  lines.push(`export const ${group.exportName} = {`);
  for (const [constName, path] of [...resolved.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
    const params = extractParams(path);
    const val = buildValue(path, group.stripForPath, params);
    lines.push(`  ${constName}: ${val},`);
  }
  lines.push('} as const;');
  lines.push('');
}

const outPath = outputArg
  ? resolve(process.cwd(), outputArg)
  : join(__dirname, '..', 'packages', 'core', 'config', 'apiPaths.generated.ts');
writeFileSync(outPath, lines.join('\n'));
console.log(`Wrote ${outPath}`);
console.log(`  ${totalPaths} path constants across ${GROUPS.length} groups`);
