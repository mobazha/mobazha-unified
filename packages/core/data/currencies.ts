// ============ 从 TOKENS 自动生成复合代币条目 ============

// 基础币种符号/名称查找表
const _baseCurrencyLookup = new Map<string, CurrencyInfo>();
BASE_CRYPTO_CURRENCIES.forEach(c => _baseCurrencyLookup.set(c.code.toUpperCase(), c));

// 链名称查找表（用于生成友好名称，如 "Tether (Ethereum)"）
const _chainNameLookup = new Map<string, string>();
CHAINS.forEach(c => {
  if (c.type === 'blockchain') _chainNameLookup.set(c.id, c.name);
});

/**
 * 从 TOKENS 自动生成的加密货币条目
 * 包括原生代币（BNB, SOL, MATIC 等）和复合代币（ETHUSDT, BSCUSDC 等）
 */
const _autoGeneratedCrypto: CurrencyInfo[] = TOKENS.filter(
  t => !_baseCurrencyLookup.has(t.id.toUpperCase())
).map(t => {
  const baseInfo = _baseCurrencyLookup.get(t.token.toUpperCase());
  const chainName = _chainNameLookup.get(t.chain) || t.chain;
  const isComposite = t.id.toUpperCase() !== t.token.toUpperCase();

  return {
    code: t.id,
    symbol: baseInfo?.symbol || t.token,
    symbolNative: baseInfo?.symbolNative || t.token,
    name: isComposite ? `${baseInfo?.name || t.token} (${chainName})` : baseInfo?.name || t.token,
    namePlural: t.id,
    decimals: t.decimals,
    rounding: 0,
    type: 'crypto' as const,
  };
});

