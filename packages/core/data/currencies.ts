/**
 * 所有货币列表
 */
export const ALL_CURRENCIES: CurrencyInfo[] = [...CRYPTO_CURRENCIES, ...FIAT_CURRENCIES];

/**
 * 货币代码到信息的映射
 */
const currencyMap = new Map<string, CurrencyInfo>();
ALL_CURRENCIES.forEach(currency => {
  currencyMap.set(currency.code.toUpperCase(), currency);
});

/**
 * 根据货币代码获取货币信息
 */
export function getCurrencyByCode(code: string): CurrencyInfo | undefined {
  return currencyMap.get(code.toUpperCase());
}

/**
 * 解析复合代币码，获取其基础货币信息
 * 例如：ETHUSDT → USDT 的 CurrencyInfo, BSCUSDC → USDC 的 CurrencyInfo
 * 如果不是复合代币码或找不到基础货币，返回 undefined
 */
function resolveTokenCurrency(code: string): CurrencyInfo | undefined {
  const baseCode = TOKEN_TO_BASE_RATE[code.toUpperCase()];
  if (baseCode) {
    return currencyMap.get(baseCode.toUpperCase());
  }
  return undefined;
}

/**
 * 获取货币符号
 * 支持复合代币码（如 ETHUSDT → ₮, BSCUSDC → USDC）
 */
export function getCurrencySymbol(code: string): string {
  const currency = getCurrencyByCode(code);
  if (currency) return currency.symbol;

  // 尝试通过 TOKEN_TO_BASE_RATE 解析复合代币码
  const baseCurrency = resolveTokenCurrency(code);
  if (baseCurrency) return baseCurrency.symbol;

  return code;
}

/**
 * 获取货币精度
 * 支持复合代币码（如 ETHUSDT → 6, BSCUSDT → 18）
 */
export function getCurrencyDecimals(code: string): number {
  const currency = getCurrencyByCode(code);
  if (currency) return currency.decimals;

  // 尝试通过 TOKEN_TO_BASE_RATE 解析复合代币码
  const baseCurrency = resolveTokenCurrency(code);
  if (baseCurrency) return baseCurrency.decimals;

  // 默认精度
  return isCryptoCurrency(code) ? 8 : 2;
}

/**
 * 判断是否为加密货币
 * 支持复合代币码（如 ETHUSDT → true）
 */
export function isCryptoCurrency(code: string): boolean {
  const currency = getCurrencyByCode(code);
  if (currency) return currency.type === 'crypto';

  // 复合代币码都是加密货币
  if (TOKEN_TO_BASE_RATE[code.toUpperCase()]) return true;

  return false;
}

/**
 * 判断是否为法币
 */
export function isFiatCurrency(code: string): boolean {
  const currency = getCurrencyByCode(code);
  if (currency) return currency.type === 'fiat';

  // 复合代币码不是法币
  if (TOKEN_TO_BASE_RATE[code.toUpperCase()]) return false;

  return false;
}

/**
 * 链上代币到基础汇率符号的映射
 * 从 TOKENS 自动推导（id !== token 的代币，如 ETHUSDT → USDT）
 * 用于汇率查询和作为未注册代币码的 fallback
 */
const TOKEN_TO_BASE_RATE: Record<string, string> = {};
// 从 TOKENS 自动推导
TOKENS.forEach(t => {
  if (t.id.toUpperCase() !== t.token.toUpperCase()) {
    TOKEN_TO_BASE_RATE[t.id.toUpperCase()] = t.token.toUpperCase();
  }
});
// 不在 TOKENS 中但需要汇率映射的代币
TOKEN_TO_BASE_RATE['CFXUSDT'] = 'USDT';
TOKEN_TO_BASE_RATE['CFXUSDC'] = 'USDC';
TOKEN_TO_BASE_RATE['MATICMBZ'] = 'MBZ';

/**
 * 获取基础汇率符号
 * 用于将链上代币映射到其基础符号以获取汇率
 */
export function getBaseRateSymbol(code: string): string {
  const upperCode = code.toUpperCase();
  return TOKEN_TO_BASE_RATE[upperCode] || upperCode;
}

/**
 * 获取常用货币列表 (用于 UI 选择器)
 */
export function getPopularCurrencies(): CurrencyInfo[] {
  const popularCodes = ['USD', 'EUR', 'CNY', 'GBP', 'JPY', 'BTC', 'ETH', 'USDT'];
  return popularCodes
    .map(code => getCurrencyByCode(code))
    .filter((c): c is CurrencyInfo => c !== undefined);
}

/**
 * 获取本地化的货币显示名称
 * 法币使用 Intl.DisplayNames API 获取本地化名称
 * 加密货币使用原始英文名称（全球通用）
 *
 * @param code 货币代码 (如 'USD', 'BTC')
 * @param locale 语言代码 (如 'en', 'zh', 'ja')
 * @returns 本地化的货币名称（纯文字，不含国旗 emoji）
 */
export function getCurrencyDisplayName(code: string, locale: string): string {
  const currency = getCurrencyByCode(code);

  // 加密货币使用原始英文名称（全球通用）
  if (currency?.type === 'crypto') {
    return currency.name;
  }

  // 法币使用 Intl.DisplayNames 获取本地化名称
  try {
    const displayNames = new Intl.DisplayNames([locale], { type: 'currency' });
    const localizedName = displayNames.of(code);
    if (localizedName) {
      return localizedName;
    }
  } catch {
    // Intl.DisplayNames 不可用时 fallback 到原始名称
  }

  // fallback: 去掉原始名称中的 emoji 前缀，只返回文字部分
  const nameStr = currency?.name || code;
  const spaceIndex = nameStr.indexOf(' ');
  if (spaceIndex > 0) {
    const prefix = nameStr.slice(0, spaceIndex);
    // 如果前缀不含 ASCII 字母，说明是 emoji 前缀
    if (!/[a-zA-Z]/.test(prefix)) {
      return nameStr.slice(spaceIndex + 1);
    }
  }
  return nameStr;
}

/**
 * 从货币代码生成国旗 emoji
 * 大多数法币代码的前两位字母对应 ISO 3166 国家代码
 * 某些国旗在部分系统上不可渲染（如 macOS 中国区的 🇹🇼），需要 UI 层处理 fallback
 */
const PROBLEMATIC_FLAG_CODES = new Set(['TWD']);

export function getCurrencyFlag(code: string): string | null {
  const currency = getCurrencyByCode(code);
  if (!currency || currency.type === 'crypto') return null;

  // 特殊处理：EUR 没有单一国家
  if (code === 'EUR') return '🇪🇺';
  // XAF, XOF 等特殊货币没有对应国旗
  if (code.startsWith('X')) return null;
  // 跳过已知不可渲染的国旗
  if (PROBLEMATIC_FLAG_CODES.has(code)) return null;

  // 从货币代码前两位生成国旗 emoji
  const countryCode = code.slice(0, 2).toUpperCase();
  try {
    return String.fromCodePoint(...countryCode.split('').map(c => 0x1f1e6 + c.charCodeAt(0) - 65));
  } catch {
    return null;
  }
}
